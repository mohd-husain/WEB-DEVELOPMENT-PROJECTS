Tailwind CSS Framework
•	Introduction to Tailwind CSS

What is Tailwind CSS
- Tailwind CSS is basically a utility-first CSS framework.
- Rapidly build modern websites without ever leaving your HTML.
- With utility classes, we can style elements on hover, focus, and more.
- It is highly customizable, low-level CSS framework.
- This CSS framework packed with classes like flex, pt-4, text-center, and rotate-90 etc.
- Its fast, flexible and reliable - with zero-runtime.
- Using responsvie utility classes.

Benifites of Taiwind CSS
- you are't wasting energy inventing class names.
- you keep your custom css files small.
- Maintaining Tailwind CSS project is much easier than maintaining a large traditinal css file
- Minumum lines of code in CSS file.
- Faster UI building process.

Bootstrap vs Tailwind CSS difference
B - It is one of the most popular HTML, CSS & JS framework used for creating responsive mobile first applications.
T - It is most popular CSS framework used for creating customized user interfaces.

B - It offers ready-made themes and templates.
T - It offers unique utility-first classes.

B - Applications created using Bootstrap are generally identical because it already has in-built templates for websites.
T - Application and websites created using Tailwind CSS are unique and flexible.

B - Bootstrap is a much older framework known for its responsiveness and efficiency. It saves a lot of time of the developers.
T - Tailwind CSS is a newer framework which is still improving and growing.

B - It requires large file size
T - It requires small file size


