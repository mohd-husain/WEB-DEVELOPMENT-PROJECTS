•	Bootstrap Break Points (XXl, xl, lg, md, sm, xs)
•	Bootstrap Break Size (Width)
- xxl (Extra Extra Large) (1400px - )
- xl (Extra Large) (1200px - 1399px)
- lg (large Screen) (992px - 1199px)
- md (Medium Device) (768px - 991px)
- sm (Small Device) (576px - 767px)
- xs (Extra Small) (0px - 575px)


•	Bootstrap Containers (.container, .container-fluid, .container-{breakpoint})


- Bootstrap comes with three different containers:
- .container-fluid, which is width: 100% at all breakpoints
- .container, which sets a max-width at each responsive breakpoint
- .container-{breakpoint}, which is width: 100% until the specified breakpoint


•	Bootstrap Grid System
- Bootstrap's grid system is built with flexbox and allows up to 12 columns across the page.

•	Bootstrap grid system is based on 12 columns (Explain)
- .col- (extra small devices - screen width less than 576px)
- .col-sm- (small devices - screen width equal to or greater than 576px)
- .col-md- (medium devices - screen width equal to or greater than 768px)
- .col-lg- (large devices - screen width equal to or greater than 992px)
- .col-xl- (xlarge devices - screen width equal to or greater than 1200px)
- .col-xxl- (xxlarge devices - screen width equal to or greater than 1400px)


